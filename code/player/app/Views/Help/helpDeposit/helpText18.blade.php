<h3>投注方式</h3>
<p>轮盘的投注区分为外围(Outside Bet)和内围(Inside Bet)，0至36号属于内围，其他则属于外围，通常为打珠一刻前截止投注（庄荷必会声明之）。</p>
<p>轮盘有多种投注方式，可单一押于一个号码、亦可押于一组号码组合，如下列各种：</p>
<p>颜色：可投注开出红或黑色号码，赔率1:1。</p>
<p>单双：可投注开出单或双数号码，赔率1:1。</p>
<p>1-18、19-36：可投注开出号码属上半（小）或下半段（大），赔率1:1。</p>
<p>12个数字组合（Dozen Bet）：可投注开出号码属于前、中或后12个号码，赔率1:2。</p>
<p>直行（Column Bet）：可投注开出号码属于第一、二或三直行，赔率1:2。</p>
<p>单个数字（Straight Bet）：投注于一个数字的格上，赔率1:35。</p>
<p>两个数字组合（Split Bet）：投注于两个数字之间的线上，赔率1:17。</p>
<p>三个数字组合（Street Bet）：投注于横行三个数字与外围投注区的线上，赔率1:11。另外，美式轮盘上投注于0,00,2三个号码也属此列，赔率一样，但英文称为Basket Bet。</p>
<p>四个数字组合（Corner Bet）：投注于四个数字交接之间的点上，赔率1:8。</p>
<p>五个数字组合（First Five Bet）：只适用于美式轮盘，用作投注0,00,1,2,3五个数字，赔率1:6。</p>
<p>六个数字组合（Sixline Bet或Alley Bet）：投注于两行横行数字与外围投注区的交接点上，赔率1:5。</p>
