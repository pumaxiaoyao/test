<h3>存款常见问题</h3>
  <p>
    1.	没有网银如何充值？<br>
    选择微信支付或支付宝存款，扫一扫即可直接到账。<br><br>
    2.	附言是什么？为什么一定要填？<br>
    在您通过转账时，请一定要在备注/附言/悄悄话/用途等处填写附言编码，否则无法自动到账。<br><br>
    3.	存款至旧的银行卡账户怎么办？<br>
    每次存款前请登录官网获取最新的银行卡账户，存至旧的银行卡账户将导致存款无法到账。<br><br>
    4.	存款一般多久到账？<br>
    填写正确的附言编码，即可实时到账，跨行用户请选择加急或实时到账才能极速到账。<br>


