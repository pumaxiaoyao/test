<h3>提款常见问题</h3>
  <p>
    1.	提款一般多久能到账？<br>
    正常情况下，提款将在10分钟内到账。<br><br>
    2.	最低提款金额是多少？<br>
    最低提款金额为100元。<br><br>
    3.	为什么任何一笔存款都需要打一倍流水？<br>
    为了防止套利者进行洗钱行为，任何一笔存款都必须一倍流水才能提款。<br><br>
    4.	为什么会提款失败？<br>
    提款失败请到交易记录页面查询原因。<br><br>
    5.	每天最高可以提款多少？<br>
    目前我们支持单笔最高5万，单日最高20万提款额度。<br><br>
    6.	可以用多少张银行卡提款？<br>
    可绑定与注册姓名一致的多张提款卡。如需解绑请点击删除银行卡。<br><br>
    7.	游戏账户里有钱为什么无法提款？<br>
    您需要先将资金从游戏钱包转至中心钱包后才能进行提款操作。<br>

