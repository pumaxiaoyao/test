  <h3>2.赛果与派彩</h3>
  <ul>
  <li>
    <p>赛果均在赛事结束后判定，除非在个别体育规则里另有注明。赛果公布72小时后，若对任何赛果有争议，本公司将不认可。在赛果公布72小时内, 除了任何体育纪律委员会所重新裁决之赛果，本公司只会修正人为、系统或参考赛果的相关网页失误等原因的错误。</p>
  </li>
  <li>
    <p>投注通常在赛事结束后派彩。但考虑到会员的利益，某些投注会在官方公布赛事结果之前就进行派彩。为此，公司保留对此而造成的错误进行更改的权利。</p>
  </li>
  <li>
    <p>派彩是根据官方来源或相关体育权威机构判定的结果为准。</p>
  </li>
  <li>
    <p>所有的交易将以公司最新备份数据记录为准，公司不接纳任何投诉或争议，除非会员提供交易记录的截图或影印证据，否则公司的数据记录将作为最终证明。</p>
  </li>
  </ul>
  <p> </p>
  <h3>3. 滚球类型投注规则</h3>
    <p>滚球投注是指对正在进行比赛的赛事进行投注。注单会在赛事进行比赛后开始接收并且在盘口关盘后停止所有交易。个别体育会开出多个滚球种类的盘口供投注。</p>
  <ul>
  <li>
    <p>会员在投注滚球时，如果赛场中出现以下几种情况，会员的投注将会维持在‘危险球-待确认’ 的状态。危险球的定义有:</p>
  </li>
  <ul>
  <li>
    <p>12码罚球(足球比赛的点球)</p>
  </li>
  <li>
    <p>自由球(攻方在守方禁区附近的自由球)</p>
  </li>
  <li>
    <p>角球 /掷入球(攻方靠近守方禁区的掷入球)</p>
  </li>
  <li>
    <p>A队向B队禁区附近进攻(或是B队在A队的禁区附近进攻)</p>
  </li>
  </ul>
  <li>
    <p>投注提交时如果遇到危险球的情况，注单上会出现"危险球-待确认"的状态。这表示注单在等待被确认或可能被取消。危险期一旦通过，并且期间没有任何显著会影响赛事状态的情况，注单就会被确认。</p>
  </li>
  <li>
<p>如果危险期内遭到进球或其他影响赛事状况的情况，比如: 红卡，所有待确认注单将会被取消。</p>
</li>
<li>
<p>在滚球投注中，本公司需强调以下条款，确保投注是按照正确的时间、赔率和在正确的情况下进行:</p>
</li>
<ul>
<li>
<p>比赛赛果和入球时间以本公司网站公布的为准，我们不参考任何其它官方网站，体育网站，或"即时比分"等网站公布的赛果或入球时间。</p>
</li>
<li>
<p>如果有合理的理由怀疑投注是在比赛时某个事件发生后才提交的，本公司将保留取消此注单且不需提供任何理由和证明的权利。</p>
</li>
<li>
<p>如果出现网站无法更新比分、赔率或盘口的情况，本公司保留权利取消所有未确认且处理中的注单。</p>
</li>
</ul>
</ul>
  <p> </p>
  <h3>4. 有关时间规则</h3>
  <ul>
<li>
<p>如比赛在法定时间提前进行，在比赛开始前的投注依然有效，在比赛开始后的所有投注均视为无效(滚球投注另作别论)。</p>
</li>
<li>
<p>足球球赛的正常完场时间包括任何球员受伤的补时。</p>
</li>
<li>
<p>除非在个别体育规则另有注明，加时得分则不计算在正常完场时间内。</p>
</li>
</ul>
  <p> </p>
  <h3>5. 并列名次规则</h3>
  <ul>
<li>
<p>在某特定赛事如果产生两名或以上的获胜者，注单派彩将按照投注本金除以获胜者人数后乘上赔率，再减去用于计算的投注本金。</p>
</li>
<li>
<p>范例：某场赛果产生了三名获胜者，投注本金将除以三， 会员的赢利便是以三分之一投注本金来计算。</p>
</li>
</ul>
  <p> </p>
  <h3>6. 场地变更</h3>
  <ul>
<li>
<p>如果比赛场地有变更（主客队调换），所有此注单将被取消。</p>
</li>
<li>
<p>本公司保留权利取消因更换场地而可能对赛事结果有影响的注单，例如：网球比赛更换场地表面。</p>
</li>
<li>
<p>若比赛原定在中立场进行改为在非中立场进行且在本公司判定下对比赛没有影响，注单将继续保持有效。</p>
</li>
<li>
<p>在个别体育项目里若有特别注明将覆盖以上规则。</p>
</li>
</ul>
  <p> </p>
  <h3>7. 错误</h3>
  <ul>
<li>
<p>本公司力求错误发生的机率保持最低，但若有注单显然是在盘口有错误的情况下提交，我们将保留取消此注单的权利。错误的情况包括：</p>
</li>
<ul>
<li>
<p>赔率错误 （和市场上提供的有明显差别）；</p>
</li>
<li>
<p>盘口信息错误， 例如：让球数，大小数等；</p>
</li>
<li>
<p>赛事信息错误，例如：参赛队名或队员， 赛事日期或开赛时间。</p>
</li>
</ul>
<li>
<p>若因遇到以上的情况而需取消任何注单，我们会尽可能的与客户取得联系，有关讯息也会及时在公告栏里发布。</p>
</li>
</ul>
  <p> </p>
  <h3>8. 异常投注行为</h3>
  <ul>
<li>
<p>对任何怀疑在投注时涉嫌作弊或破坏本公司投注平台的会员，公司有权在毫无警告或通知下取消此会员所有的注单并且冻结账户。异常行为包括使用任何设备，自动设备，软件，程序等方式干涉本网站的运作。</p>
</li>
</ul>
