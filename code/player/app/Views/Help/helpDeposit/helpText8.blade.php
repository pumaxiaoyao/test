  <h3>1.一般体育规则 </h3>
  <p>所有在本公司进行的投注都需要依照以下规则与条款处理。在个别体育项目里注明的规则将视为体育主要规则:</p>
  <ul>
    <li>
      <p>所有投注项目的最高和最低投注额将由公司决定，如有任何更改无需提前通知。</p>
    </li>
    <li>
      <p>会员申请账户时需提供正确的个人信息，本公司对提供伪造或错误信息的账户将不负任何责任。</p>
    </li>
    <li>
      <p>会员将全权负责账户提交的所有交易。在投注前请仔细检查选项，一旦投注提交成功后，将无法更改或取消。公司对会员自身原因造成的遗漏或重复投注不负任何责任。会员可以在"交易记录"中查看详情确保所有提交的注单已成功。</p>
    </li>
    <li>
      <p>在任何投诉中，如果在公司的数据库中没有存储任何记录，公司将不接受也不认可任何会员提供的复印件或单据。</p>
    </li>
    <li>
      <p>公司保留在任何时候关闭或冻结会员账号的权利。</p>
    </li>
    <li>
      <p>公司保留在任何时候暂停/中止会员对任何盘口进行投注的权利。</p>
    </li>
    <li>
      <p>公司保留对已预先知道赛果的投注作出取消的权利。如果由于"滚球现场"延迟而引起盘口的赔率错误，此期间的注单将视为无效。</p>
    </li>
    <li>
      <p>相关赛事的信息（例如：日期，时间，比分，数据，新闻，红卡，中立范围详情）仅是一般公共信息。公司将不会对提供信息的准确性承担任何责任。</p>
    </li>
