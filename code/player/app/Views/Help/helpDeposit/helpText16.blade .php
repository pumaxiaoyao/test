<h3>投注</h3>
<p>百家乐投注人数没有严格上限，除了9人参与（小桌）或14人参与（大桌，两边圆弧各7人）赌场提供坐位以外，在旁站立者亦可自由参与投注。不过当庄家与闲家投注双方注码超过赌桌所限之限红时，荷官会在发牌前点算庄家、闲家、对子和之投注区注码（俗称数红），以示意超过该台限定金额（限定金额在每张赌桌均有示意），并著客人减注至限定金额以内方才开局，除了设定最高限红外，每张赌桌亦设有最低投注额。</p>
<p>而玩家如认为庄闲开出点数一样，不能分出胜负，可押注和局（Tie）；玩家如认为庄、闲任一方（或两方同时）所获发的牌会出现一对，如两张3或两张Q，可押注对子（Pair），同样并无限制（10与J、Q或K或J、Q、K任意两张所组成的牌虽然皆作0点，但并不算作对子）。</p>
<p>另外庄家或闲家之投注注码最大之坐位者有权自行、指定任一有投注之坐位或者由荷官揭开底牌（站立投注者不能揭开底牌）；而部分赌场之赌桌只能由荷官揭开底牌（这些在该张赌桌上均有说明）。</p>
<h3>发牌与博牌</h3>
<p>百家乐一般用8副牌，洗牌后8副牌放在发牌箱内。庄、闲家双方每局均会收到至少两张牌，但不超过三张。第一及第三张牌发给“闲家”，第二及第四张牌则发给“庄家”。至于要否博牌则根据博牌规则（或称牌例）决定。百家乐跟廿一点不同，玩家可下注于庄（Banker）或闲（Player），并无限制。</p>
<h3>点数计算方法</h3>
<p>在百家乐中，Ace的扑克牌被算作1点；从2到9的扑克牌依点数不变，均依照其显示的点数计算；10、J、Q及K的扑克牌则被算作零点（有些赌场以10点计）。当所有牌的点数总和超过9时，则只算总数中的个位。因此，一个8和一个9的牌点大小为：7点（8 + 9 = 17）。因百家乐中只计算扑克牌的个位数值，因此可能的最大点数为9点（如一个4 和一个 5：4 + 5 = 9），最少则为0点，又称baccarat（如一个10 和一个 Q：10 + 10 = 20，只算个位是 0）。</p>
<h3>赔彩</h3>
<p>赔彩时首两张牌决定对子，然后根据博牌规则出现最后结果，以点数多寡决定庄、闲获胜或和局（包含若有第三张牌发牌的情况）；玩家下注的一方另如所得点数是8或9点（即例牌），此时即可决定庄闲之胜负。</p>
<h3>赔率</h3>
<p>下注庄家而庄赢者，赢1赔0.95。</p>
<p>下注闲家而闲赢者，赢1赔1。</p>
<p>下注和局（即最终点数一样者），赢1赔8。</p>
<p>下注对子（即庄或闲首两张牌为同数字或英文字母者），赢1赔11。</p>
