<h3>路单</h3>
<p>把百家乐的牌局发展记录下来，一般人叫做『写路』，一般路分：珠盘路、大路、大眼仔路、小路 而赌场亦会给各位赌客一张路纸，以便投注人士用自己的方法记录牌局发展。</p>
<p>有些朋友会用两种不同颜色记录庄赢和闲赢的出现次数或其他不同的旺弱变数，他们便跟据这些记录来分析，并撑握它的规律来下注。其实写路只是增强信心的一种下注方法，赢始终要运气和心态，不过你有信心便增加盈利的机会，加快运气来临，因为赌钱不可能由头赢到尾，除非你赌一局就走 （但这是最高的玩法，可惜很少人可以做到）所以你要在有信心的一局押大注，而在没有信心的一局押小注或不下注，静待运气来临，亦就是说希望各位朋友可以做到输少赢大，当然不是有信心押大注就一定赢。但如果用平均注码的赌法去投注，输的比例会比大细注大。</p>
