<h3 style="margin: 0">百家乐</h3>
</div>
<div>
<p>百家乐，英文为Baccarat，baccarat在意大利语中的意思就是“0”，源起于法国的一种纸牌游戏，流行于欧洲各地赌场。20世纪由叶汉先生将Baccarat从美国引入澳门，并为其起了一个具有东方色彩的名字－－百家乐。时至今日，百家乐是世界各地赌场中受欢迎的赌戏之一。在澳门的赌场中，百家乐赌桌的数目更是全球赌场之中最多。</p>
<h3>基本简介</h3>
<p>使用3～8副，每副52张纸牌，洗在一起，置於发牌盒中，由荷官从其中分发。各家力争手中有两三张牌总点数为9或接近9，K、Q、J和10都计为0，其他牌按牌面计点。计算时，将各家手中的牌值相加，但仅论最後一位数字。当场付赌金最多者为庄家。</p>
<p>20世纪，澳门叶汉先生将Baccarat从美国引入澳门，并为其起了一个具有东方色彩的名字－－百家乐。</p>
<p>百家乐游戏分为庄（Bank）、闲（Play）、和（Tie）与对子（Pair）四门，另外部分赌场有其独特的押注方式，比如大小、庄双、庄单、闲双、闲单。这里的庄、闲，并没有具体的含义，只是代表游戏的双方，和与对子则是为了增加娱乐性而设立的一个彩头。客人根据自己的想法可任意选择庄、闲、和与对子或其他任意一门下注。</p>
<h3>定义</h3>
<p>百家乐英文为Baccarat，是世界上公认的最文明、最公平的娱乐项目。谁的两张牌加起来的总数最接近9，谁就赢。印有人像和10字样的牌可以按0计分，所以在意大利语中的意思就是“0”。</p>
<h3>发展历程</h3>
<p>百家乐原本在远古时期欧洲国家的一种游戏，至于是意大利还是法国人发明的已经是无从查证了，百家乐是一种很古老的游戏，几个世纪一来相继在各大洲的各个国家兴起流行，规则也是不断地变化和改进，以至于发展成现在的游戏规则。</p>
<p>我们在各个国家都可以看到有人玩百家乐游戏，在早些时期法国的百家乐版本最为流行，法国版本“Chemin de fer”当时在英国流行起来，当然法国版的百家乐也要入乡随俗了，经过了英国人的规则改变，英国人又把这种游戏称为：“欧洲百家乐”。紧接着百家乐又辗转来到了南美洲。在六十年代末，一个叫Tommy的老外，把一种叫做Chemin的游戏带到了美国，这次带来的百家乐是Chemin，与“欧洲百家乐”相结合的产物，六十年代自古巴关停了哈瓦那的之后，又在沙丘之地美国的拉斯维加斯悄然兴起，然而合法化。尽管百家乐在各个国家的的规则都有所不同，但是大致的风格原貌还是没有变的，任期自己的高雅性，在欧洲各国很受一些上流社会人士的青睐，成了各大玩家的一个必玩的游戏之一。</p>
<p>而后20世纪60年代初，百家乐又从美国被带到了澳门，从此在中国的土地上得到繁荣发展。一个姓叶的先生把美国的游戏Baccarat引进到澳门，并且给他取了一个响亮的名字“百家乐”。</p>
<p>时至今日，百家乐是世界各地赌场中受欢迎的赌戏之一。在澳门的赌场中，百家乐赌桌的数目更是全球赌场之中最多。</p>
